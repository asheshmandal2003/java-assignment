package operators;

public class Q1 {
    public void arithmeticOperations(int num1, int num2) {
        System.out.printf("num1 + num2 = %d\n", num1 + num2);
        System.out.printf("num1 - num2 = %d\n", num1 - num2);
        System.out.printf("num1 * num2 = %d\n", num1 * num2);
        System.out.printf("num1 / num2 = %d\n", num1 / num2);
    }
}
