package operators;

public class Q5 {
    public void logicalOperators(boolean statement1, boolean statement2) {
        System.out.printf("OR operation: %b\n", statement1 | statement2);
        System.out.printf("AND operation: %b\n", statement1 & statement2);
        System.out.printf("Not operation of statement1: %b\n", !statement1);
        System.out.printf("Not operation of statement2: %b\n", !statement2);
    }
}
